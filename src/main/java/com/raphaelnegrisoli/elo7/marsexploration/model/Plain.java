package com.raphaelnegrisoli.elo7.marsexploration.model;

public class Plain {

    private final int width;
    private final int height;

    public Plain(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {

        return width;
    }

    public int getHeight() {

        return height;
    }
}
